    public Image getImage() {
        Image image = webcam.getImage();
        if (image != null) {
            lblScreen.setIcon(new ImageIcon(new ImageIcon(image).getImage().getScaledInstance(lblScreen.getWidth(), lblScreen.getHeight(), Image.SCALE_DEFAULT)));
            return image;
        }
        return null;
    }

    public void record() {
        webcam.open();
        while (true) {
            Image image = webcam.getImage();
            if (image != null) {
                lblScreen.setIcon(new ImageIcon(new ImageIcon(image).getImage().getScaledInstance(lblScreen.getWidth(), lblScreen.getHeight(), Image.SCALE_DEFAULT)));
            }

            try {
                Thread.sleep(100);
            } catch (InterruptedException ex) {
                ex.printStackTrace();
            }
        }
    }

